"""
    Operações matemáticas
"""

valor1 = 25
valor2 = 10

soma = valor1 + valor2
subtracao = valor1 - valor2
multiplicacao = valor1 * valor2
divisao = valor1 / valor2
divisao_inteiro = valor1 // valor2
resto_divisao_valor1_valor2 = valor1 % valor2
valor1_elevado_valor2 = valor1 ** valor2
valor1_raiz_quadrada = valor1 ** (1/2)
valor2_raiz_cubica = valor2 ** (1/3)

print("Soma -> valor1 + valor2:", soma)
print("Subtração -> valor1 - valor2:", subtracao)
print("Multiplicação -> valor1 * valor2:", multiplicacao)
print("Divisão -> valor1 / valor2:", divisao)
print("Divisão inteira -> valor1 // valor2:", divisao_inteiro)
print("Módulo -> valor1 % valor2:", resto_divisao_valor1_valor2)
print("Potenciação -> valor1 ** valor2:", valor1_elevado_valor2)
print("Radiciação -> valor1 ** (1/2):", valor1_raiz_quadrada)
print("Radiciação -> valor2 ** (1/3):", valor2_raiz_cubica)